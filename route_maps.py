def step(position: str, operation: str, status_after: str | None = None):
    return {
        "position": position,
        "operation": operation,
        "status_after": status_after or f"После операции: {operation}",
    }


def route(*production_steps):
    return [*CUTTING_ROUTE, *production_steps, *PACKING_ROUTE]


